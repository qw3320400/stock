var _ Strategy = &MytestStrategy{}

type MytestStrategy struct {
	DefaultStrategy
	// internal
	leftValue     float64
	cost          float64
	inv           float64
	lastValueList []float64
}

func (s *MytestStrategy) Init() error {
	s.Tag += "_test_inv"
	return s.DefaultStrategy.Init()
}

func (s *MytestStrategy) Step() (bool, error) {
	if s == nil || s.baostockLocalData == nil || s.stepIndex < 0 {
		return false, utils.Errorf(nil, "param error %+v", s)
	}
	if s.Result == nil {
		s.Result = &StrategyResult{
			LineData: []*PointData{},
		}
	}
	if s.lastValueList == nil {
		s.lastValueList = []float64{}
	}
	if int64(len(s.baostockLocalData.StockKDateList)) < s.stepIndex+1 {
		return false, nil
	}
	point := &PointData{
		Time: s.baostockLocalData.StockKDateList[s.stepIndex].TimeCST,
	}
	closeStr := s.baostockLocalData.StockKDateList[s.stepIndex].Close
	close, err := strconv.ParseFloat(closeStr, 64)
	if err != nil {
		return false, utils.Errorf(err, "strconv.ParseFloat fail")
	}
	s.lastValueList = append(s.lastValueList, close)

	baseTime, err := time.Parse("2006-01-02", "2006-01-01")
	if err != nil {
		return false, utils.Errorf(err, "time.Parse fail")
	}
	days := int64(s.baostockLocalData.StockKDateList[s.stepIndex].TimeCST.Sub(baseTime) / time.Hour / 24)
	baseRate := close / base[days] / 1000
	invRate := float64(0)
	if baseRate > 1.5 {
		invRate = 0
	} else if baseRate > 1.3 {
		invRate = 0.2
	} else if baseRate > 1.2 {
		invRate = 0.3
	} else if baseRate > 1.1 {
		invRate = 0.4
	} else if baseRate > 1.0 {
		invRate = 0.5
	} else if baseRate > 0.9 {
		invRate = 0.6
	} else if baseRate > 0.8 {
		invRate = 0.7
	} else if baseRate > 0.7 {
		invRate = 0.8
	} else if baseRate > 0.5 {
		invRate = 1
	} else {
		invRate = 1
	}
	if s.stepIndex == 0 {
		s.leftValue = 1
	}
	if s.cost != 0 {
		point.Value = s.inv*close/s.cost + s.leftValue
	} else {
		point.Value = s.leftValue
	}
	newInv := (s.leftValue + s.inv) * invRate
	if newInv > s.inv {
		s.cost = s.cost*s.inv/newInv + close*(newInv-s.inv)/newInv
		s.leftValue -= (newInv - s.inv)
		s.inv = newInv
	} else if newInv < s.inv {
		s.leftValue += (s.inv - newInv)
		s.inv = newInv
	}

	s.Result.LineData = append(s.Result.LineData, point)
	s.stepIndex++
	return true, nil
}
